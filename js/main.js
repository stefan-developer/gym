	
	// JavaScript code
	function openSidebar() {
	    document.getElementById('my-sidebar-menu').style.display
	    ='block';

	    document.getElementById('sidebar-overlay').style.display
	    ='block';
	}

	function closeSidebar() {
	    document.getElementById('my-sidebar-menu').style.display
	    ='none';

	    document.getElementById('sidebar-overlay').style.display
	    ='none';
	}

	